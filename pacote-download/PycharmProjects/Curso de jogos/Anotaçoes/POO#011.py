### Programação Orientada a Objetos(POO)

# É uma forma diferente de se programar

# Característica de linguagens de alto nível (Python, C#, etc...)

# Permite uma melhor reutilização de código

# Menos linhas de códigos

# Representa melhor o mundo real

## Sopa de letrinhas da POO:
# -Classes
# -Atributos
# -Métodos
# -Objetos
# -Herança
...
## Classes
# São um grupo de individos, obj, de qualquer coisa que tem caracteristicas em comum, exemplo:
# Classe cachorro:
# Atributos da Classe cachorro: todos cachorro tem nariz, rabo, orelhas. Essas são as o que chamam de atributos da classe
# Além de Atributos tem tambem o que a gente chama de métodos
# Métodos: São ações que os individuos que pertence a essa classe ralisao, exemplo: Latir, corre e comer
# Na programaçao os Atributos = Variaveis e os Métodos = Funçoes
# Diferentes objts da mesma Classe Cachorro: preto, branco, maior, menor
# Mesmo que os cachorros sejam da mesma classe vai existir difença entre eles

