### *Operadores Aritméticos* ###

# + Adição
# - Subtração
# * Mutiplicação
# / Divisão
# ** Potência (3 ** 2 = 3 ao quadradado)
# // Divisão Inteira
# % Resto da Divisão

##EXEMPLOS-01!!!##
5 + 2 == 7
5 - 2 == 3
5 * 2 == 10
5 / 2 == 2.5
5 ** 2 == 25
5 // 2 == 2
5 % 2 == 1

