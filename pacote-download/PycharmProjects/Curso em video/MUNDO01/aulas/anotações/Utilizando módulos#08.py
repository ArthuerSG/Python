### Utilizando módulos

# Para importa uma biblioteca é assim:
# import *biblioteca*

# Para importa uma coisa da biblioteca é assim:
# from *biblioteca* import *modulo*

# Biblioteca match # match >>> matematica
# ceil >>> faz arredondamento para cima
# floor >>> faz arrendondamento para baixo
# trunc >>> truncar um numero, vai eliminar da virgula para frente sem arrendondar
# pow >>> potencia, similar ao os 2 **
# sqrt >>> serve para calcular a raiz quadrada
# factorial >>> ?(pesquisar no google)?

